'use client'

import { useEffect, useState } from 'react'
import { useRouter } from 'next/navigation'
import toast, { Toaster } from 'react-hot-toast'
import { formatCurrency } from '@/lib/utils'
import type { InventoryItem } from '@/types'

interface POItem extends InventoryItem {
  quantity_to_order: number
  selected: boolean
}

interface SupplierGroup {
  name: string
  email: string
  items: POItem[]
}

export default function PurchaseOrderPage() {
  const router = useRouter()
  const [items, setItems] = useState<POItem[]>([])
  const [loading, setLoading] = useState(true)
  const [sending, setSending] = useState<string | null>(null)

  useEffect(() => {
    fetch('/api/inventory')
      .then((r) => r.json())
      .then((d) => {
        const lowStock = (d.items ?? [])
          .filter((i: InventoryItem) => i.quantity <= i.reorder_threshold)
          .map((i: InventoryItem) => ({
            ...i,
            quantity_to_order: Math.max(1, i.reorder_threshold - i.quantity + 5),
            selected: true,
          }))
        setItems(lowStock)
        setLoading(false)
      })
  }, [])

  function setQty(id: string, qty: number) {
    setItems((prev) => prev.map((i) => i.id === id ? { ...i, quantity_to_order: Math.max(1, qty) } : i))
  }

  function setEmail(supplier: string, email: string) {
    setItems((prev) => prev.map((i) => i.supplier === supplier ? { ...i, supplier_email: email } : i))
  }

  function toggleItem(id: string) {
    setItems((prev) => prev.map((i) => i.id === id ? { ...i, selected: !i.selected } : i))
  }

  async function sendPO(supplier: string) {
    const group = items.filter((i) => i.supplier === supplier && i.selected)
    const email = group[0]?.supplier_email
    if (!email) { toast.error('Enter supplier email first'); return }

    setSending(supplier)
    const res = await fetch('/api/inventory/purchase-order', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        supplier,
        supplierEmail: email,
        items: group.map((i) => ({
          part_name: i.part_name,
          sku: i.sku,
          quantity_to_order: i.quantity_to_order,
          cost_price: i.cost_price,
        })),
      }),
    })
    const data = await res.json()
    if (!res.ok) toast.error(data.error ?? 'Failed to send PO')
    else toast.success(`PO ${data.poRef} sent to ${email}`)
    setSending(null)
  }

  function printPO(supplier: string) {
    const group = items.filter((i) => i.supplier === supplier && i.selected)
    const shopName = process.env.NEXT_PUBLIC_APP_NAME ?? 'Repair Shop'
    const poRef = `PO-${Date.now()}`
    const rows = group.map((i) =>
      `<tr><td style="padding:8px 12px;border-bottom:1px solid #e5e7eb">${i.part_name}</td><td style="padding:8px 12px;border-bottom:1px solid #e5e7eb;font-family:monospace">${i.sku ?? '—'}</td><td style="padding:8px 12px;border-bottom:1px solid #e5e7eb;text-align:center;font-weight:bold">${i.quantity_to_order}</td><td style="padding:8px 12px;border-bottom:1px solid #e5e7eb;text-align:right">${i.cost_price != null ? `£${i.cost_price.toFixed(2)}` : '—'}</td></tr>`
    ).join('')
    const html = `<!DOCTYPE html><html><body style="font-family:sans-serif;padding:40px;max-width:700px;margin:auto"><div style="display:flex;justify-content:space-between;border-bottom:2px solid #000;padding-bottom:20px;margin-bottom:20px"><div><h1 style="margin:0">${shopName}</h1></div><div style="text-align:right"><p style="margin:0;font-size:13px;color:#666">Purchase Order</p><p style="margin:4px 0;font-size:24px;font-weight:bold;font-family:monospace">${poRef}</p></div></div><p><strong>To:</strong> ${supplier}</p><table width="100%" style="border-collapse:collapse;margin-top:20px"><thead><tr style="background:#f3f4f6"><th style="padding:10px 12px;text-align:left;font-size:13px">Part</th><th style="padding:10px 12px;text-align:left;font-size:13px">SKU</th><th style="padding:10px 12px;text-align:center;font-size:13px">Qty</th><th style="padding:10px 12px;text-align:right;font-size:13px">Unit Cost</th></tr></thead><tbody>${rows}</tbody></table><p style="margin-top:30px;color:#666;font-size:13px">Generated by ${shopName}</p></body></html>`
    const w = window.open('', '_blank')
    if (w) { w.document.write(html); w.document.close(); w.print() }
  }

  if (loading) {
    return (
      <div className="flex items-center justify-center py-32">
        <div className="w-8 h-8 border-2 border-primary border-t-transparent rounded-full animate-spin" />
      </div>
    )
  }

  // Group by supplier
  const suppliers: Record<string, InventoryItem[]> = {}
  for (const item of items) {
    const s = item.supplier ?? 'Unknown Supplier'
    if (!suppliers[s]) suppliers[s] = []
    suppliers[s].push(item)
  }

  const supplierGroups: SupplierGroup[] = Object.entries(suppliers).map(([name, groupItems]) => ({
    name,
    email: (groupItems[0] as POItem).supplier_email ?? '',
    items: groupItems as POItem[],
  }))

  return (
    <>
      <Toaster position="top-center" toastOptions={{ style: { background: '#18181b', color: '#fafafa', border: '1px solid #3f3f46' } }} />

      <div className="space-y-6 max-w-4xl">
        <div className="flex items-center gap-4">
          <button onClick={() => router.back()} className="text-muted hover:text-fg transition-colors">
            <svg width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
              <path d="m15 18-6-6 6-6"/>
            </svg>
          </button>
          <div>
            <h1 className="text-2xl font-bold text-fg">Purchase Orders</h1>
            <p className="text-muted text-sm">{items.length} low-stock part{items.length !== 1 ? 's' : ''} across {supplierGroups.length} supplier{supplierGroups.length !== 1 ? 's' : ''}</p>
          </div>
        </div>

        {items.length === 0 && (
          <div className="card text-center py-12">
            <p className="text-muted">All stock levels are OK — no items need reordering.</p>
          </div>
        )}

        {supplierGroups.map((group) => (
          <div key={group.name} className="card space-y-4">
            <div className="flex items-start justify-between gap-4 flex-wrap">
              <div>
                <h2 className="font-semibold text-fg">{group.name}</h2>
                <p className="text-xs text-muted">{group.items.filter((i) => i.selected).length} item{group.items.filter((i) => i.selected).length !== 1 ? 's' : ''} selected</p>
              </div>
              <div className="flex gap-2">
                <button
                  onClick={() => printPO(group.name)}
                  className="btn-secondary text-sm flex items-center gap-2"
                >
                  <svg width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
                    <path d="M6 18H4a2 2 0 0 1-2-2v-5a2 2 0 0 1 2-2h16a2 2 0 0 1 2 2v5a2 2 0 0 1-2 2h-2"/>
                    <path d="M6 9V3a1 1 0 0 1 1-1h10a1 1 0 0 1 1 1v6"/><rect x="6" y="14" width="12" height="8" rx="1"/>
                  </svg>
                  Print PO
                </button>
                <button
                  onClick={() => sendPO(group.name)}
                  disabled={sending === group.name}
                  className="btn-primary text-sm flex items-center gap-2"
                >
                  <svg width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
                    <path d="m22 2-7 20-4-9-9-4Z"/><path d="M22 2 11 13"/>
                  </svg>
                  {sending === group.name ? 'Sending…' : 'Email PO'}
                </button>
              </div>
            </div>

            {/* Supplier email */}
            <div className="flex gap-3 items-center">
              <label className="text-xs text-muted whitespace-nowrap">Supplier email</label>
              <input
                type="email"
                className="input text-sm flex-1 max-w-xs"
                placeholder="supplier@example.com"
                value={group.email}
                onChange={(e) => setEmail(group.name, e.target.value)}
              />
            </div>

            {/* Items table */}
            <div className="overflow-x-auto">
              <table className="w-full text-sm">
                <thead>
                  <tr className="border-b border-border">
                    <th className="text-left text-muted font-medium py-2 pr-4 w-8" />
                    <th className="text-left text-muted font-medium py-2 pr-4">Part</th>
                    <th className="text-left text-muted font-medium py-2 pr-4 hidden sm:table-cell">SKU</th>
                    <th className="text-center text-muted font-medium py-2 pr-4">In Stock</th>
                    <th className="text-center text-muted font-medium py-2 pr-4">Order Qty</th>
                    <th className="text-right text-muted font-medium py-2">Unit Cost</th>
                  </tr>
                </thead>
                <tbody className="divide-y divide-border">
                  {group.items.map((item) => (
                    <tr key={item.id} className={item.selected ? '' : 'opacity-40'}>
                      <td className="py-2 pr-4">
                        <input
                          type="checkbox"
                          checked={item.selected}
                          onChange={() => toggleItem(item.id)}
                          className="accent-primary cursor-pointer"
                        />
                      </td>
                      <td className="py-2 pr-4 text-fg">{item.part_name}</td>
                      <td className="py-2 pr-4 text-muted font-mono text-xs hidden sm:table-cell">{item.sku ?? '—'}</td>
                      <td className="py-2 pr-4 text-center text-warning font-bold">{item.quantity}</td>
                      <td className="py-2 pr-4 text-center">
                        <input
                          type="number"
                          min="1"
                          value={item.quantity_to_order}
                          onChange={(e) => setQty(item.id, parseInt(e.target.value) || 1)}
                          className="input w-16 text-sm text-center py-1"
                          disabled={!item.selected}
                        />
                      </td>
                      <td className="py-2 text-right text-muted">{formatCurrency(item.cost_price)}</td>
                    </tr>
                  ))}
                </tbody>
              </table>
            </div>
          </div>
        ))}
      </div>
    </>
  )
}
